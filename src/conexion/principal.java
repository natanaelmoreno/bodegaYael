    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
       String usuario=tusuario.getText();
       String password=tcontra.getText();
       if((usuario.isEmpty())||(password.isEmpty()))
       {
         JOptionPane.showMessageDialog(null, "Ingrese su nombre de usuario y contrase침a");
       }
       else
       {
         conexion conectar = new conexion();
         Statement st =conectar.conectar();

       try
       {

         ResultSet rs = st.executeQuery("SELECT * FROM login WHERE usuario='"+usuario+"' AND password ='"+password+"'");
         rs.last();

         int encontrado=rs.getRow();
         //System.out.println(String.valueOf(encontrado));
         if(encontrado==1)
         {
           this.setVisible(true);
          dialog_login.setVisible(false);
         }
         else
         {
             JOptionPane.showMessageDialog(null, "Sus datos son incorrectos, reviselos");
         }

         rs.close();
         st.close();
       }
       catch (Exception e)
      {
          e.printStackTrace();
      }


       }
    }//GEN-LAST:event_jButton1ActionPerformed

